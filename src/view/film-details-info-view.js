export default class FilmDetailsInfoView {
  #element = null;
  #popupMovie = null;
  
  constructor (popupMovie) {
    this.#popupMovie = popupMovie;
  }

  get template () {
    return createFilmDetailsInfoTemplate(this.#popupMovie);
  }

  get element () {
    if (!this.#element) {
      this.#element = createElement(this.template);
    }

    return this.#element;
  }

  removeElement () {
    this.#element = null;
  }
}
